void OLED_Init(void)
{
    HAL_Delay(100);
    OLED_WR_CMD(0xAE); 
    OLED_WR_CMD(0xD5); OLED_WR_CMD(0x80);
    OLED_WR_CMD(0xA8); OLED_WR_CMD(0x3F);
    OLED_WR_CMD(0xD3); OLED_WR_CMD(0x00);
    OLED_WR_CMD(0x40); 
    OLED_WR_CMD(0x8D); OLED_WR_CMD(0x14);
    OLED_WR_CMD(0x20); OLED_WR_CMD(0x00); // 🔥水平地址模式（关键！）
    OLED_WR_CMD(0xA1); 
    OLED_WR_CMD(0xC8);
    OLED_WR_CMD(0xDA); OLED_WR_CMD(0x12);
    OLED_WR_CMD(0x81); OLED_WR_CMD(0xCF);
    OLED_WR_CMD(0xD9); OLED_WR_CMD(0xF1);
    OLED_WR_CMD(0xDB); OLED_WR_CMD(0x30);
    OLED_WR_CMD(0xA4);
    OLED_WR_CMD(0xA6);
    OLED_Clear();
    OLED_WR_CMD(0xAF);
    HAL_Delay(100);
}

void OLED_Clear(void)
{
    uint8_t y, x;
    for(y=0;y<8;y++){
        OLED_WR_CMD(0xB0+y);
        OLED_WR_CMD(0x00);
        OLED_WR_CMD(0x10);
        for(x=0;x<128;x++) OLED_WR_DATA(0x00);
    }
}

void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t c)
{
    uint8_t i;
    c -= 32;
    OLED_WR_CMD(0xB0 + y);
    OLED_WR_CMD(0x00 + (x&0x0F));
    OLED_WR_CMD(0x10 + (x>>4));
    for(i=0;i<6;i++) OLED_WR_DATA(OLED_ASCII6x8[c][i]);
}

void OLED_ShowStr(uint8_t x, uint8_t y, char *str)
{
    while(*str)
    {
        OLED_ShowChar(x,y,*str);
        x += 6;
        if(x>122) {x=0;y++;}
        str++;
    }
}

